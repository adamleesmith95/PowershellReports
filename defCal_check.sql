SELECT 
	d.DeferralCalendarCode ,
	CONVERT(DATE,d.RevenueRecognitionDate) ,
	c.*
FROM dbo.DeferralCalendarDate d
full  OUTER JOIN #correct_def_cals c
ON d.DeferralCalendarCode = c.def_cal_code
AND d.RevenueRecognitionDate = c.rev_DATE

WHERE d.DeferralCalendarCode IN (SELECT i.def_call_code FROM #call_to_CHECK i)
AND c.def_cal_code IS null


