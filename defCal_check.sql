SELECT 
	c.* ,
	d.DeferralCalendarCode ,
	d.RevenueRecognitionDate
FROM #correct_def_cals c
LEFT OUTER JOIN dbo.DeferralCalendarDate d
ON c.def_cal_code = d.DeferralCalendarCode
AND c.rev_DATE = d.RevenueRecognitionDate

WHERE d.DeferralCalendarCode IS null

